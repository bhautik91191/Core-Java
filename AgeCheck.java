package com.corejava;

/**
* Define an 'age' variable and check your age
* Uses if / else condition
*
* @author  Bhautik Padmani
* @version 1.8
* @since   March 2014 
*/
public class AgeCheck {

	public static void main(String[] args) {
		int ageSize = 30; // Age is a variable with value 30 

		// If / else condition use
		if (ageSize < 25) {
			System.out.println("Your Age is 25 Down");
		} else {
			System.out.println("Your Age is 25 Up");
		}
	}

}
